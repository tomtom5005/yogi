## Functions as Delegates

Delegates. They're everywhere. Drummed into the heads of Objective-C (and Java)
programmers is this message: use protocols (interfaces) for callbacks. You
define a protocol, the delegate implements that protocol, and it registers
itself as the delegate so that it gets callbacks.

If a delegate has a simple method, it can mechanically be replaced by a
function. However, there are a number of tradeoffs to keep in mind.

